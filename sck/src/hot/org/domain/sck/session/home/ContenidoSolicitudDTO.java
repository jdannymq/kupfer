package org.domain.sck.session.home;

import java.util.Date;

import org.domain.sck.entity.LcredSolicitud;
public class ContenidoSolicitudDTO {

	private LcredSolicitud solicitud;
	private String destinoNombre;
	private String destinoEmail;
	private Date fechaContacto;
	private String mensajeDetalle;



	public String getDestinoNombre() {
		return destinoNombre;
	}

	public void setDestinoNombre(String destinoNombre) {
		this.destinoNombre = destinoNombre;
	}

	public String getDestinoEmail() {
		return destinoEmail;
	}

	public void setDestinoEmail(String destinoEmail) {
		this.destinoEmail = destinoEmail;
	}

	public Date getFechaContacto() {
		return fechaContacto;
	}

	public void setFechaContacto(Date fechaContacto) {
		this.fechaContacto = fechaContacto;
	}

	public String getMensajeDetalle() {
		return mensajeDetalle;
	}

	public void setMensajeDetalle(String mensajeDetalle) {
		this.mensajeDetalle = mensajeDetalle;
	}

	public LcredSolicitud getSolicitud() {
		return solicitud;
	}

	public void setSolicitud(LcredSolicitud solicitud) {
		this.solicitud = solicitud;
	}

	
}