package org.domain.sck.dto;

import java.io.File;

import org.domain.sck.entity.Attachment;
import org.domain.sck.entity.LcredTipoSolicitud;
import org.richfaces.model.UploadItem;

public class FileUploadedDTO {
	


	private LcredTipoSolicitud tipo;
	private UploadItem uploadItem;
	private String nombreArchivo;
	private String rutaArchivo;
	private boolean eliminar;
	private Attachment archivo;
	private File archivoAux;
	
	public FileUploadedDTO() {
	}
	
	public LcredTipoSolicitud getTipo() {
		return tipo;
	}
	public void setTipo(LcredTipoSolicitud tipo) {
		this.tipo = tipo;
	}
	
	public UploadItem getUploadItem() {
		return uploadItem;
	}
	
	public void setUploadItem(UploadItem uploadItem) {
		this.uploadItem = uploadItem;
	}
	
	public String getNombreArchivo() {
		return nombreArchivo;
	}
	
	public void setNombreArchivo(String nombreArchivo) {
		this.nombreArchivo = nombreArchivo;
	}

	public boolean isEliminar() {
		return eliminar;
	}

	public void setEliminar(boolean eliminar) {
		this.eliminar = eliminar;
	}

	public String getRutaArchivo() {
		return rutaArchivo;
	}

	public void setRutaArchivo(String rutaArchivo) {
		this.rutaArchivo = rutaArchivo;
	}

	public Attachment getArchivo() {
		return archivo;
	}

	public void setArchivo(Attachment archivo) {
		this.archivo = archivo;
	}

	public File getArchivoAux() {
		return archivoAux;
	}

	public void setArchivoAux(File archivoAux) {
		this.archivoAux = archivoAux;
	}

	
	
	
	
	
}
