package org.domain.sck.dto;


//DTO datos de cormportamiento comercial de la solicitud seleccionada
public class CompComercialDTO {
	
	/* morosidad */
	private long montoTotalMoro;
	private long numeroMoro;
	private long diasMoras;
	/*protesto*/
	private long montoTotalProt;
	private long numeroProt;
	private String dmPort;
	
	public CompComercialDTO() {
		
	}

	public long getMontoTotalMoro() {
		return montoTotalMoro;
	}

	public void setMontoTotalMoro(long montoTotalMoro) {
		this.montoTotalMoro = montoTotalMoro;
	}

	public long getNumeroMoro() {
		return numeroMoro;
	}

	public void setNumeroMoro(long numeroMoro) {
		this.numeroMoro = numeroMoro;
	}

	public long getDiasMoras() {
		return diasMoras;
	}

	public void setDiasMoras(long diasMoras) {
		this.diasMoras = diasMoras;
	}

	public long getMontoTotalProt() {
		return montoTotalProt;
	}

	public void setMontoTotalProt(long montoTotalProt) {
		this.montoTotalProt = montoTotalProt;
	}

	public long getNumeroProt() {
		return numeroProt;
	}

	public void setNumeroProt(long numeroProt) {
		this.numeroProt = numeroProt;
	}

	public String getDmPort() {
		return dmPort;
	}

	public void setDmPort(String dmPort) {
		this.dmPort = dmPort;
	}

	
	

}
