package org.domain.sck.dto;

public class DetalleCPAgrupadoDTO {
	
	public DetalleCPAgrupadoDTO(){}
	
	private String nomNegocio;
	private String nomConcepto;
	private Long monto;
	private Double margen;
	private Double margenProdproceso;

	

	public Double getMargen() {
		return margen;
	}
	public void setMargen(Double margen) {
		this.margen = margen;
	}
	
	public Double getMargenProdproceso() {
		return margenProdproceso;
	}
	public void setMargenProdproceso(Double margenProdproceso) {
		this.margenProdproceso = margenProdproceso;
	}
	public void setMonto(Long monto) {
		this.monto = monto;
	}
	public Long getMonto() {
		return monto;
	}
	public void setNomNegocio(String nomNegocio) {
		this.nomNegocio = nomNegocio;
	}
	public String getNomNegocio() {
		return nomNegocio;
	}
	
	public String getNomConcepto() {
		return nomConcepto;
	}
	
	public void setNomConcepto(String nomConcepto) {
		this.nomConcepto = nomConcepto;
	}
	
}
